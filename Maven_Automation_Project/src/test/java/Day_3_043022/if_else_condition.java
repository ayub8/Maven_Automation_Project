/**
 * The class is called if_else_condition and it has a main method.
 */
public class if_else_condition {

    public static void main(String[] args) {

        // Declaring two variables, a and b, and assigning them values.
        int a = 5;
        int b = 3;

        //print when a is less than b or print when it's greater/equal to b
        if (a < b)
        {
            System.out.println("a is less than b");
        }
        else
        {
            System.out.println("a is greater or equal b");
        }

    }//end of the main
}//end of the class
